            <p>El exfutbolista Diego Armando Maradona pidió este jueves la salida del suizo Joseph Blatter de la presidencia de la FIFA, lo calificó de corrupto y confió en el triunfo del príncipe jordano Ali Bin Al Hussein, a quien apoya para las elecciones en el máximo organismo rector del balompié mundial.</p>

            <p>Maradona, quien llegó a Bogotá para jugar este viernes en la noche un partido en apoyo a la paz en Colombia, criticó fuertemente la gestión del actual presidente de la FIFA y sostuvo que la candidatura del príncipe Ali tiene muchas posibilidades de ganar en las elecciones del próximo 29 de mayo, en las que Blatter buscará su quinto mandato.</p>

            <p>Lee: ¿Podrá una agencia de comunicación desbancar a Joseph Blatter de la FIFA?</p>

            <p>"Los cortos no los voy a abandonar jamás (...) pero estoy con muchas más ganas de ir ahora con el príncipe Ali a pelearle la FIFA al corrupto de Blatter", sostuvo en una conferencia de prensa en Bogotá.</p>
            <p>Maradona, quien lideró a Argentina para ganar el Mundial de 1986 en México y brilló en el Napoli de Italia, dijo que apoya al príncipe jordano porque "no tiene necesidad de robar, tiene su dinero, y tiene mucho. No tiene necesidad de hacer lo que hace Blatter, de creerse el todo poderoso del futbol cuando no pateó una pelota".</p>

            <p>El argentino afirmó que el proyecto de su candidato incluye la construcción de canchas de futbol en sectores pobres de países de África y de América Latina como Colombia y Ecuador para apoyar a los niños y buscar talentos.</p>

            <p>"Yo creo que tenemos muchas chances de pegarle una patada en el culo a Blatter, sin ninguna duda", comentó Maradona en medio de risas de los asistentes.</p>

            <p>"Estoy motivado para ir a decirle Blatter: Ándate, basta, ya está (...) ya nos hiciste mucho daño", aseguró el exfutbolista de 54 años de edad.</p>

            <p>Por otra parte Maradona, quien actualmente presenta un programa de televisión, pidió el fin del conflicto armado en Colombia con la negociación de paz que mantienen el gobierno y la guerrilla de las FARC en Cuba.</p>

            <p>En el partido para apoyar la paz en Colombia participarán junto a Maradona viejas glorias del futbol de este país sudamericano, pero estarán ausentes por compromisos deportivos las principales figuras internacionales del momento como James Rodríguez y Radamel Falcao García.</p>

            <p>"Me siento orgulloso de poder estar, de poder ser una pieza más de un partido por la paz que Colombia ya está mereciendo, ya está en las puertas de la paz, basta de violencia", dijo el argentino.</p>

            <p>"Hoy más que nunca Colombia quiere la paz y el mundo quiere la paz para Colombia, eso es lo más importante", agregó al insistir que es importante apoyar a los hombres y mujeres que buscan poner fin a un conflicto armado de más de medio siglo a través de la negociación.</p>

            <p>Maradona elogió a la selección colombiana de futbol que avanzó hasta cuartos de final en el Mundial de Brasil y sostuvo que la ausencia por lesión del delantero Falcao García impidió que ganará la Copa del Mundo.</p>

            <p>"El Pelusa" se refirió a James Rodríguez, mediocampista del Real Madrid y goleador del pasado Mundial como "un zurdo, talentoso, exquisito y elegante".</p>

